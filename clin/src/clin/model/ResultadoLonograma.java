package clin.model;

import javax.persistence.Entity;
import javax.persistence.PrimaryKeyJoinColumn;

@Entity
@PrimaryKeyJoinColumn(name = "ResultadoExame_idResultadoExame")
public class ResultadoLonograma extends ResultadoExame {

	private String k;
	private String na;
	private String ra;
	private String bicarbonato;

	public String getK() {
		return k;
	}

	public void setK(String k) {
		this.k = k;
	}

	public String getNa() {
		return na;
	}

	public void setNa(String na) {
		this.na = na;
	}

	public String getRa() {
		return ra;
	}

	public void setRa(String ra) {
		this.ra = ra;
	}

	public String getBicarbonato() {
		return bicarbonato;
	}

	public void setBicarbonato(String bicarbonato) {
		this.bicarbonato = bicarbonato;
	}

}
