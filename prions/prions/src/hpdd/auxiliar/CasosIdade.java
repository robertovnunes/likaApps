package hpdd.auxiliar;

public class CasosIdade {
	public String idade;
	public long numCasos;
	
	public CasosIdade(String idade, long numCasos) {
		this.idade = idade;
		this.numCasos = numCasos;
	}
	
	public long getNumCasos() {
		return numCasos;
	}

	public void setNumCasos(long numCasos) {
		this.numCasos = numCasos;
	}

	public String getIdade() {
		return idade;
	}

	public void setIdade(String idade) {
		this.idade = idade;
	}
}
